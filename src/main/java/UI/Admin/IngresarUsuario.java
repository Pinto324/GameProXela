    public void funcionamientoBoton(){
        if (!(jTextFieldNombre.getText().equals("")) && !(jTextFieldUsername.getText().equals(""))&& !(jPasswordField.getText().equals(""))) {
            if(jComboBoxTipo.getSelectedIndex()==1){
                if(!(jTextFieldNoCaja.getText().equals(""))){
                    if(controlador.IngresarUsuario(jTextFieldNombre.getText(), (jComboBoxTipo.getSelectedIndex()+1), jTextFieldUsername.getText(), jPasswordField.getText(), Integer.valueOf(jComboBoxSucursal.getSelectedIndex()+1),Integer.valueOf(jTextFieldNoCaja.getText()))){
                    JOptionPane.showMessageDialog(null, "se creo el usuario correctamente", "aviso", JOptionPane.INFORMATION_MESSAGE);
                    }else{
                        JOptionPane.showMessageDialog(null, "hubo un error en la creaci√≥n del usuario", "aviso", JOptionPane.INFORMATION_MESSAGE);
                    }                
                }else{
                    JOptionPane.showMessageDialog(null, "Un campo est√° vacio", "Error", JOptionPane.INFORMATION_MESSAGE);
                }
            }else{
                if(controlador.IngresarUsuario(jTextFieldNombre.getText(), (jComboBoxTipo.getSelectedIndex()+1), jTextFieldUsername.getText(), jPasswordField.getText(), Integer.valueOf(jComboBoxSucursal.getSelectedIndex()+1),-1)){
                    JOptionPane.showMessageDialog(null, "se creo el usuario correctamente", "aviso", JOptionPane.INFORMATION_MESSAGE);
                    }else{
                        JOptionPane.showMessageDialog(null, "hubo un error en la creaci√≥n del usuario", "aviso", JOptionPane.INFORMATION_MESSAGE);
                    } 
            }
        } else {
            JOptionPane.showMessageDialog(null, "Un campo est√° vacio", "Error", JOptionPane.INFORMATION_MESSAGE);
        }
    }
