    public void funcionamientoBoton() {
        switch (jComboBoxReporte.getSelectedIndex()) {
            case 0:
                if (jDateChooserInicio.getDate() == null || jDateChooserFinal.getDate() == null) {
                    JOptionPane.showMessageDialog(null, "Por favor, selecciona ambas fechas.", "error", JOptionPane.ERROR_MESSAGE);
                } else if (jDateChooserInicio.getDate().after(jDateChooserFinal.getDate())) {
                    JOptionPane.showMessageDialog(null, "La fecha de inicio no puede ser posterior a la fecha de finalizaci√≥n.", "error", JOptionPane.ERROR_MESSAGE);
                } else {
                    // Formatear y mostrar las fechas en consola
                    SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
                    String fechaInicio = dateFormat.format(jDateChooserInicio.getDate());
                    String fechaFin = dateFormat.format(jDateChooserFinal.getDate());
                    ArrayList<String> datosTabla = controlador.ReporteDescuentos(fechaInicio, fechaFin);
                    DefaultTableModel model = new DefaultTableModel();
                    model.addColumn("Numero de factura");
                    model.addColumn("Fecha");
                    model.addColumn("Nombre comprador");
                    model.addColumn("Nombre de caja");
                    model.addColumn("Nit");
                    model.addColumn("Total");
                    model.addColumn("Descuento");
                    model.addColumn("Total con descuento");
                    for (int i = 0; i < datosTabla.size(); i += 8) {
                        Object[] fila = {datosTabla.get(i), datosTabla.get(i + 1), datosTabla.get(i + 2), datosTabla.get(i + 3), datosTabla.get(i + 4), datosTabla.get(i + 5), datosTabla.get(i + 6), datosTabla.get(i + 7)};
                        model.addRow(fila);
                    }
                    jTable1.setModel(model);
                }
                break;
            case 1:
                if (jDateChooserInicio.getDate() == null || jDateChooserFinal.getDate() == null) {
                    JOptionPane.showMessageDialog(null, "Por favor, selecciona ambas fechas.", "error", JOptionPane.ERROR_MESSAGE);
                } else if (jDateChooserInicio.getDate().after(jDateChooserFinal.getDate())) {
                    JOptionPane.showMessageDialog(null, "La fecha de inicio no puede ser posterior a la fecha de finalizaci√≥n.", "error", JOptionPane.ERROR_MESSAGE);
                } else {
                    SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
                    String fechaInicio = dateFormat.format(jDateChooserInicio.getDate());
                    String fechaFin = dateFormat.format(jDateChooserFinal.getDate());
                    ArrayList<String> datosTabla = controlador.ReporteTop10(fechaInicio, fechaFin);
                    DefaultTableModel model = new DefaultTableModel();
                    model.addColumn("Numero de factura");
                    model.addColumn("Fecha");
                    model.addColumn("Nombre comprador");
                    model.addColumn("Nombre de caja");
                    model.addColumn("Nit");
                    model.addColumn("Total con descuento");
                    for (int i = 0; i < datosTabla.size(); i += 8) {
                        Object[] fila = {datosTabla.get(i), datosTabla.get(i + 1), datosTabla.get(i + 2), datosTabla.get(i + 3), datosTabla.get(i + 4), datosTabla.get(i + 5), datosTabla.get(i + 6), datosTabla.get(i + 7)};
                        model.addRow(fila);
                    }
                    jTable1.setModel(model);
                }
                break;
            default:
                break;
        }
    }

